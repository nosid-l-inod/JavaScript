// Conditional in JavaScript
// Conditionals are use to check if an expression returns true or not

let iceCream = "chocolate";

if (iceCream === "chocolate") {
    console.log("Its a chocolate icecream");
}
else if (iceCream === "Strawberry") {
    console.log("This is a strawberry icecream");
}
else {
    console.log("This is not my favorite");
}